# 第一题是给两个长度一样的数组，第二个数组可以调整顺序，让你调整，使两个数组对应位相乘，相加得到的值最小，输出最小值。

# 第二题是有52个字母，即a-z和A-Z，从里面取子集构成一个字符串。输入的就是这个字符串，然后现在对字符串进行剔除，
#      剔除规则是，字符串里面有些字母既有大写又有小写，对于这些字母（出现“两次”）可以选择剔除掉其中一个字母，
#      然后问你怎么剔除得到字母序（大小写不影响，最后输出小写）最小的字符串，输出字符串的第一个字母。

# 第三题是给n个银行的坐标(一维)和银行里有的钱，相隔大于d的银行才能抢，两个强盗，问怎么选择使得抢的钱最多。

# 第四题是给两个字符串(由左括号和右括号组成，可能不匹配),然后组合成新字符串(要求匹配的),
